    <h2>No puedo saber, sólo creer</h2>
    <p>
        Esta canción la compuse cuando estudiaba en la Facultad de Filosofía y Letras de la UNAM, en el primer semestre de Filosofía me encontraba estudiando <em>el problema de otras mentes</em>, un problema espistemológico que afirma que no es posible comprobar la existencia de otras mentes además de la de uno mismo. Esta afirmación nos deja en una posición filosófica llamada solipsismo. El caso es que esta canción habla sobre cómo este pensamiento puede llegar a ser tan abrumador: darse cuenta de que, de hecho, no puedes refutar que las personas con las que convives no son más que autómatas, seres sin mente que sólo están ahí para aparentar que son personas. Sin embargo, esa afirmación sólo es una posibilidad y creer que eso es verdad es arbitrario y está injustificado, ya que tampoco puedes comprobar que las personas con las que convives sean de hecho autómatas. Es esta incertidumbre de lo que habla esta canción, de esa mera posibilidad y el agobio que puede llegar a causar.

    <h2>No somos seres racionales <small><em>(Pensamientos dispersos)</em></small></h2>
    <p>
        Somos seres emocionales, no somos seres racionales. Por mucho tiempo nos hemos llamado "seres racionales" a nosotros mismos, pensando que la razón debería gobernarnos siempre, cuando esto no podría ser cierto. Sólo hacemos uso de la razón para satisfacer fines emocionales. Lo que verdaderamente sería un ser racional —siendo algún tipo de ser— es una computadora, y una computadora no tiene sentido sin un usuario. La razón es una herramienta, no es lo que somos. La razón por sí sola no hace nada, la razón sin contexto es inútil, sin sentido. Lo que le da sentido a la razón son las emociones, esos impulsos que nos obligan a actuar. La razón sólo es un medio para un fin, la razón no puede ni debería ser considerada como un fin en sí mismo, porque la razón funciona sólo cuando se le dan principios, esos puntos de partida, sean definiciones, emociones, valores. Lo difícil es cuando los principios chocan. Podríamos poner como un principio ampliamiente aceptado la supervivencia de la raza humana. ¿Para qué queremos sobrevivir como especie?, no lo sabemos con certeza, por regla general los humanos queremos sobrevivir, nos causa terror la desaparición, el absurdo de que nuestros esfuerzos sean para nada, no poder trascender. No importa que sea un deseo que no pueda justificarse racionalmente (de manera epistémica), lo deseamos, y por esto la supervivencia de la humanidad es un principio que nos parece bello a la mayoría. 
    </p>
    <p>
        Así como el principio de sobrevivir, tenemos otros en común la mayoría de los seres humanos. ¿Por lo tanto tenemos derecho a imponer estos principios a todos los humanos, sin importar las minorías que difieran? La verdad es que yo no lo sé. Sin embargo, un mundo donde todos "jalan parejo" es un poco menos complicado, o al menos es fácil creer que lo sería. Es un tema muy delicado porque no se puede generalizar irresponsablemente. Recordemos que Hitler quería lograr lo que él consideraba que era lo "bueno", lo que debía ser. Pero creo que la mayoría de los humanos actuales concordaríamos en que mucho de lo que hizo nos causa horror, hizo cosas muy "malas". ¿Nosotros entonces somos los buenos? Creo que el querer reducir todo a "bueno" o "malo" es inconveniente para el pensamiento crítico. Creo que debemos ser concientes de que el bien y el mal son conceptos completamente humanos, que hacen referencia a emociones positivas y negativas, respectivamente. Yo creo que el bien y el mal no existen fuera de nosotros, es fácil observar que el Universo es indiferente del bien y del mal.
    </p>


            <h3>Letra</h3>
            <pre>
Tiempo, deja de correr así que 
aún hay tanto por hacer
y parece que el día ya no alcanza
para todo lo que vendrá.

Y no parará, 
y no parará,
tu tiempo pasará.

Esa sensación de que algo 
pisa mis talones
se acompaña de mucha confusión.

Al momento de ver
los años que dejé
se aturde todo mi ser.
Después, con este acervo
espiritual y cultural,
no hay lugar para pensar, 
sólo actuar y actuar.


Bien, y con tanto por hacer
no hay tiempo que perder
es lo que se me impulsa a creer
no hay tiempo que perder
pues este mundo te rezagará.

            </pre>

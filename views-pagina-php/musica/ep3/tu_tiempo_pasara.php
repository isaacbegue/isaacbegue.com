            <h3>Comentarios</h3>
            <p>
                La idea de <em>Tu tiempo pasará</em> salió un día mientras ensayábamos en Capital Orbeats. Yo estaba tocando el bajo, repasando los efectos de la pedalera y me encontré con ese efecto con el filtro, comencé a tocar el riff y Jorge Neville me siguió en la batería. Grabamos la idea para ponerle letra después. Cuando se acabó el proyecto de Capital Orbeats decidí retomar la idea de esa canción porque me gustaba mucho.
            </p>
            <p>
                La letra creo que es bastante autodescriptiva. De cómo se busca ir más rápido y más rápido para ser más productivo y hacer más con menos y volver a repetir el proceso de optimización <em>ad infinitum</em>. No habla sobre que sea algo malo en sí misma esta aceleración, sino que cómo se siente el enfrentarse a ésta aceleración. Cómo se siente vivir en un mundo donde todo el tiempo tienes que estarte actualizando y renovando porque el día que lo dejes de hacer, simplemente serás obsoleto, entonces el descanso ya no parece algo saludable, sino más bien como un estorbo, "descansaré en mi vejez" o se puede interpretar como un "descansaré cuando ya no sea útil". Y reitero, no es una crítica reduccionista de bueno o malo, el mundo tiende a eso y tiene sus razones de ser, el punto es ¿hasta dónde es necesario para el individuo seguir este juego?, estoy seguro de que la vida es mucho más que sólo nuestro trabajo y muy importante poder equilibrar y nutrir cada área de nuestras vidas.
            </p>

            <h3>Comentarios</h3>
            <p>
                Esta canción describe una vida "Godín", y lo vacía que puede llegar a ser si uno sólo dedica su vida al trabajo, a la búsqueda de bienes materiales como un fin en sí mismo  y/o a la búsqueda de posicionamiento y reconocimiento social, otra vez, como fin en sí mismo. Creo que es bastante autodescriptiva.
            </p>
            <p>
                Ya es lunes es también una canción de hace ya varios años, al rededor del 2011, según logro recordar. Apenas ahora que he logrado adaptarla y estructurarla bien es cuando por fin he quedado satisfecho con el resultado.

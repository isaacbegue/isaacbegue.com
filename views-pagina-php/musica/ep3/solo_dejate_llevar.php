            <h3>Comentarios</h3>
            <p>
                El rasgueo de guitarra me salió por ahí del 2011, si no mal recuerdo. En ese entonces también tenía muy arraigada de usar el ritmo de songo en la batería para alguna canción. Cuando estuve experimentando con el delay y el pitcher  de la guitarra me salió el arreglo del precoro y el segundo verso. Con esas tres ideas decidí hacer una canción, aunque no tenía todavía la idea para la letra. Por ese entonces acababa de ver la película de <em>Trainspotting</em> y me gustó mucho la canción de <em>Born Slippy</em> de Underworld que sale al final, así que tomé la idea de una melodía similar para los versos.
            </p>
            <p>
                La letra en general habla de disfrutar el ahora, así como podemos extrañar el pasado, probablemente en el futuro también se extrañaremos los momentos que estamos viviendo ahora.

            <h3>Comentarios</h3>
            <p>
                La Pena Capital habla sobre cómo es muy fácil abusar del poder cuando se lo tiene y de la impotencia que se experimenta cuando uno no puede defenderse ante ese tipo de abusos. "Acepta ya tu suerte" puede interpretarse como sarcasmo, a manera de reto, para hacer lo contrario y buscar resistir junto a un movimiento, o bien, de manera literal y ocuparse sólo de lo que está en nuestro control de manera personal. ¿Qué es lo mejor? Yo no lo sé, eso lo decides tú. Por mi parte no creo que se pueda estar en una posición estática al respecto, habrá casos en los que sea necesario la resistencia, apoyando una causa o grupo, y habrá otros casos en los que lo mejor será ocuparse de lo que uno puede cambiar individualmente.
            </p>

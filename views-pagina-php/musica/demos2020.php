            <h3>Comentarios</h3>
            <p>
                Este EP fue el primero que subí a las plataformas digitales. Vi un video en YouTube de cómo hacerlo y el precio me pareció adecuado. El intermediario con el que yo ahorita tengo mis canciones es DistroKid. El EP contiene las canciones que tenía más pulidas hasta el momento. Los temas centrales de las canciones son de caracter filosófico.
            </p>
            <p>
                Una de las pautas en la grabación de las canciones del EP es el de ajustar o componer las canciones con la idea de que puedan ser tocadas por un power trío de batería, bajo y guitarra. Esta pauta vino de la idea de que sería más fácil conseguir a un bajista y un baterísta que me acompañara en los eventos en vivo que conseguir más músicos. De hecho casi siempre esa es una de mis pautas al componer música, el ser realista con los recursos y no sobreproducirme. Ahora que he decidido presentarme sólo yo en los eventos en vivo, trato de componer con la sólo con la base de una caja de ritmos y todo lo demas tocarlo yo con ayuda de loopers. Esta otra pauta viene de la dificultad que implica el tener que conseguir músicos de acompañamiento, el gestionar ensayos y quedar con gente que hay que estar acarreando es una de las razones por las que decidí hacerme solista. Ahora si se me llega a presentar un evento ya tengo en mente que tocaré con secuencias con la batería y el bajo, y ya si uno de mis amigos músicos está disponible y me puede acompañar, pues qué mejor.
            </p>
            <p>
                <a target="_blank" href="https://twitter.com/audiocreativa">César Díaz</a> me ayudó a detallar varias de las canciones de este EP. La portada del álbum me la tomó Luar Cipriano, novio en aquél tiempo de una amiga mía, en una sesión de fotos para el proyecto.
            </p>

            <h3>Comentarios</h3>
            <p>
                Este EP lo armé con las composiciones más recientes que tenía en el 2020. Me enfoqué en el minimalismo, con la idea de que sería yo solo acompañado de una caja de ritmos y loopers. La portada la dibujé sobre una hoja de una libreta que nos regalaron en la <em>Facultad de Filosofía y Letras</em> allá por el 2014, una página tenía un "4", y un día estaba aburrido comencé a colorear de negro con la pluma alrededor de éste; no sé por qué pero considero al 4 mi número favorito. Esos rayones estuvieron ahí hasta que se me ocurrió la idea de usarlos como portada para el EP, ya que justamente son 4 canciones las que tenía preparadas en ese entonces.
            </p>
            <p>
                Mi influencia principal al componer estas canciones fue Radiohead y también Thom Yorke de solista.

            <h3>Comentarios</h3>
            <p>
                Caída Libre surgió con el ritmo de batería y un riff que empecé a tocar encima de éste. El riff me gustó bastante, me sonaba como a Los Kinks y también un poco a Nirvana. Sobre esta base comencé a balbucear una melodía. La canción me transmitía energía con un poco de ironía. Decidí seguir esas  ideas para componer la letra.
            </p>
            <p>
                &nbsp;&nbsp; La letra habla sobre la perseverancia, y eso es prácticamente todo de lo que habla. Cuando la escucho o la canto me gusta pensar en <em>Dark Souls III</em>, es un videojuego que llevo ya varios días jugando ahorita en diciembre 2021. De hecho creo que la letra encaja muy bien con la filosofía del juego: "No dejes de intentarlo", se pueden sacar unas reflexiones muy inspiradoras de este juego y me gusta creer que me ha ayudado a poner a prueba mi persistencia, que siento que ha sido una de mis debilidades en algunas áreas de mi vida. 
            </p>
            <p>    
                &nbsp;&nbsp; Trato de recordarme esta canción cuando siento que lo que hago no está valiendo la pena, para ayudarme a combatir ese sentimiento de derrota. Y por supuesto, de la mano viene también a la mente <em>Dark Souls III</em>.
            </p>

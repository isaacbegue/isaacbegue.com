            <h3>Comentarios</h3>
            <p>
                En febrero del 2020 me encontraba leyendo el libro de <em>La Peste</em> de Albert Camus. El libro me transmitió muchas emociones acerca de cómo se vive una pandemia. Curiosamente en ese entonces todavía no tenía idea de que en apenas un mes viviría una pademia yo mismo —aunque afortunadamente no viví una tan horrorosa como la del libro o como la que vivieron muchas personas que sí perdieron familiares a causa del COVID-19—. Compuse la canción cuando terminé el libro, quería transmitir esa desesperación, esa incertidumbre al estar encerrado y desinformado, viendo de pronto cómo las personas entran en el hospital y no se vuelve a saber de ellas o escuchando que un vecino ya se contagió y murió. Y al final una resignación sobre lo que queda, la posibilidad de que a uno también le toque partir o la esperanza de sobrevivir y tratar de retomar la vida cuando la pandemia termine.
            </p>
            <p>
                &nbsp;&nbsp; Respecto a la música, usé la misma técnica que estuve usando en todo este álbum. Tenía en mi caja de ritmos uno que me llamó la atención y quería usarlo en una canción. Comencé a desplegar ideas con la guitarra y balbuceé melodías hasta que me convencieron al estar acorde a lo que quería transmitir. La letra, como ya dije, fue inspirada en <em>La Peste</em> de Albert Camus.
            </p>
            <p>    
                &nbsp;&nbsp; <small>*No sé si en todos los países hispanohablantes 'internar' tenga la misma connotación, al menos en México se usa 'internar' cuando alguien ingresa al hospital para ser tratado. De cualquier forma, a eso se refiere ese verso en la canción.</small>

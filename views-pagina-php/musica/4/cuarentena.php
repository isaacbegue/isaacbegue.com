            <h3>Letra</h3>
            <pre>
    Mientras te miraba,
    mientras me ahogué,
    mientras tú decías
    "todo estará bien". 

    Cuando te internaron luego
    ¿qué sucederá?
    Ya no será igual,
    somos recuerdos,
    somos recuerdos,
    somos recuerdos
    y nada puedo hacer,
    deja al tiempo correr.

    Frente a mis ojos pasó 
    una y otra vez,
    y aún no sabemos 
    cuándo acabará
    ni a cuántos más se llevará.


    Mientras esperaba,
    entre tantos más,
    el día funesto
    o el final de la prisión.

            <h3>Comentarios</h3>
            <p>
                El 'riff' principal de esta canción surgió cuando estaba <em>jameando</em> sobre ese mismo ritmo de batería que tiene la canción. Sobre el riff comencé a improvisar arreglos en guitarra y luego canté una melodía balbuceando cosas, como suelo hacerlo. En este caso la melodía y la interpretación que daba al balbucear me transmitía una sensación de resignación, de cansancio, de hastío. Entonces traté de transmitir lo mismo al componer la letra.
            </p>
            <p>
                &nbsp;&nbsp;Esta letra habla sobre ponerse en una posición estoica respecto de las cosas que no dependen de nosotros. Una clase de aceptación, con tintes utilitarios, de la vida actual. Si bien es importante la reflexión y tener una brújula de cómo nos gustaría que fuese el mundo, también debemos ser conscientes de que las utopías sólo son castillos en el aire y el querer aplicar utopías en 'la vida real', sin importar los medios o los matices, la más de las veces conlleva consecuencias llenas de horror, sobre todo cuando se tiene el poder para intentar llevarlas a cabo. 
            </p>
            <p>    
                &nbsp;&nbsp;Los problemas del mundo nunca se acabarán —a menos que nos extingamos, entonces ya no habría nadie quien se preocupase de las cuestiones humanas, ¿o sí?, no lo sé—, hay que aprender a vivir con ello. Esta canción habla sobre buscar la paz, aceptar que como individuos aislados nunca podremos solucionar los grandes problemas del mundo, y no tenemos porqué poner esa carga sólo sobre nuestros hombros. Sin embargo sí hay cosas que podemos cambiar y que dependen de nosotros y es ahí donde debemos poner 'nuestro grano de arena', donde deberíamos enfocar nuestros esfuerzos individuales.
            </p>

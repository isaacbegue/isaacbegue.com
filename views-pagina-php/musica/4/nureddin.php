            <h3>Comentarios</h3>
            <p>
                En <em>El libro de las mil noches y una noche</em> se encuentra un relato contado por Scherezada llamado <em>Historia del visir Nureddin, de su hermano el visir Chamseddin y de Hassán Badreddin</em> —sí, así de largo es el título—, sobre esa historia trata esta canción. Es una de las historias que más me ha gustado dentro de lo que he leído de <em>El libro de las mil noches y una noche</em> —actualmente (diciembre 2021) voy en el tomo XI, lo leo cuando tengo ratos libres, es una meta personal terminar los 23 tomos, es otra área en la que pongo a prueba mi perseverancia, hay muchas historias muy buenas dentro de este librote— así que esa fue mi inspiración para el tema que le daría a la canción que sólo le faltaba letra.
            </p>
            <p>
                &nbsp;&nbsp; La canción comenzó con la base de la batería, es un ritmo que me gustó mucho en la caja de ritmos de mi pedalera. Para hacer los arreglos de la guitarra me inspiré mucho en la forma en la que toca Jonny Greenwood, el guitarrista de Radiohead, me parece un gran referente musical.
            </p>

            <h3>Comentarios</h3>
            <p>
                Este arreglo lo hice tomando la versión acústica que grabó en vivo Rockdrigo González y le puse la batería de <em>Tomorrow Never Knows</em> de Los Beatles. Me gustó mucho la combinación resultante, ya sólo añadí un bajo también parecido al de la canción de Los Beatles y en los coros traté de ponerle un arreglo interesante. Lo considero como un tributo al buen Rockdrigo, las canciones que he escuchado de él tienen unas letras muy interesantes. Me gustaría hacer también hacer una adaptación de <em>No tengo tiempo</em> me identifico mucho con la letra de esa canción, creo que somos mayoría los que nos sentimos así hoy en día, determinados por La Máquina.

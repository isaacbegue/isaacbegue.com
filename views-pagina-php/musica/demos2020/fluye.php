            <h3>Comentarios</h3>
            <p>
                Esta canción comenzó con el riff. Cuando iba a la <em>Facultad de Filosofía y Letras</em> tocaba en los camiones de camino a la facultad, así me ahorraba el pasaje y ganaba un poco de dinero. En uno de esos momentos en lo que esperaba el siguiente camión me puse a improvisar con la guitarra y salió ese riff. El riff se me figuraba mucho al estilo Oasis —que es una de mis bandas favoritas— así que quise sacar una canción de este riff, grabé con el celular la idea y la dejé para cuando me llegara la inspiración.
            </p>
            <p>
                &nbsp;&nbsp;En una primer versión de la canción la letra era muy abstracta y no me terminaba de convencer. En un momento de inspiración me llegó la idea de hacer la canción menos cargada y más emocional, a la Oasis en su primer álbum. Tomé algunas palabras clave de las canciones del <em>Definitely Maybe</em> y traté de emular a Oasis. Definitivamente me gustó más el resultado. La letra habla sobre ser proactivo, de tener ganas de ser y de hacer.
            </p>
            <p>    
                &nbsp;&nbsp;La versión final de la canción se logró cuando estuve trabajando junto a mi amigo <a target="_blank" href="https://twitter.com/audiocreativa">César Díaz</a>, quien fue mi manager por un tiempo y me estuvo acompañando a tocadas y ayudándome con arreglos en varias canciones. Con la que más me ayudó fue con esta canción, sugiriendo cambiar el ritmo de la batería por algo más sencillo, ya que en primeras versiones la batería iba con un doble golpe de tarola. La idea era imaginar que quien tocaba la batería era alguien como Ringo Starr, tocando justo lo que la canción necesita, sin ningún elemento de sobra. Así que con esa grandiosa idea me puse a grabar la batería tratando de transmitir ese <em>feeling</em> a la Ringo Starr. Seguido de la batería, nos pusimos a trabajar en el solo de guitarra. El cual la primera parte se repetía, él sugirió que no se repitiera y que hiciera una variación. Estuve improvisando hasta que llegamos con la forma actual del solo y nos satisfizo el resultado.
            </p>

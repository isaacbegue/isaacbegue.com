            <h3>Comentarios</h3>
            <p>
                Esta canción habla sobre el ejercicio de la reflexión, acerca de lo intrincado y complejo que es organizar y depurar las ideas en la mente. Muchas veces nos encontraremos divagando en la aporía, lidiando con la contradicción al darnos cuenta de cómo algunas de nuestras ideas o creencias fundamentales se contradicen. Acerca de cómo el mundo nos llama a actuar aún cuando no tengamos tiempo de analizar lo que creemos, así que entra la arbitrariedad y damos muchas cosas por hecho sin tener en cuenta un escritinio a esas ideas, de poner en duda nuestras creencias. Pero volvemos con el tiempo, cosa rara que parece nunca ser suficiente, necesitamos actuar y para justificarnos necesitamos alguna base, el problema es que no tenemos tiempo de analizar todas nuestras bases, todos nuestros principios. De eso habla esta canción.
            </p>
            <p>
                &nbsp;&nbsp; <a target="_blank" href="https://twitter.com/audiocreativa">César Díaz</a>, amigo mío que es ingeniero en audio y con quien estuve trabajando, me ayudó con la batería de la canción. Me dio bastantes ideas para los remates y detalles que enriquecieron bastante la composición de la batería.
            </p>
            <p>    
                &nbsp;&nbsp;Originalmente esta canción la había compuesto para una novia que tuve cuando estaba en la Prepa. La letra base es muy similar, sólo que en la primer versión la canción hablaba de cómo quería que la relación mejorara. Al final esta canción y una segunda que le compuse, llamada <em>Mi Luna</em>, nunca las pudo escuchar porque terminamos antes de que se presentara la oportunidad de enseñárselas y yo estaba muy enojado y sentido en aquel entonces, y después de un tiempo ya no tuvo sentido compartírselas. Sin embargo, como me gustaba mucho el ritmo de <em>Entiéndeme</em> unos años después decidí retomar la base de la canción para reestructurarla y cambiarle la letra por un tema que me llamara más la atención.
            </p>

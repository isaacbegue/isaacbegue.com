            <h3>Comentarios</h3>
            <p>
                Esta canción la compuse junto a mi amigo <a target="_blank" href="https://www.linkedin.com/in/jes%C3%BAs-eduardo-l%C3%B3pez-escalona/">Eduardo López Escalona</a>. El riff del comienzo, junto como el cambio de tempo y la idea del llanto en el coro los compuso él. Originalmente toda la base de la canción con su letra estuvo compuesta por Lalo y ésta se llamaba <em>Confusión</em> —ahí en el respaldo tengo la versión original— y yo le ayudé a grabarla y a tocar la batería, él la cantó, tocó las guitarras y el bajo. Por alguna u otra razón no continuamos trabajando en la canción. Un día retomé la idea de Lalo y la reestructuré combinándola con un arreglo que tenía por ahí que usé para el coro y con un solo al final. Le cambié la letra con una tonada parecida a la original, pero no me convencía, así que luego la volví a cambiar con otra melodía. A Lalo también le gustó esta nueva versión y de hecho la tocábamos cuando estabamos con la banda Capital Orbeats. Esta última versión que viene en el EP ya es una versión adaptada para tocarse con batería, bajo, guitarra y una voz, acorde a las pautas que me propuse al grabar las canciones para este EP.
            </p>
            <p>
                &nbsp;&nbsp;La canción habla sobre la pérdida de un ser querido, creo que la letra en sí misma es bastante descriptiva y no necesita mayor explicación.
            </p>
            <p>
                Acá <a target="_blank" href="https://twitter.com/audiocreativa">César Díaz</a> también me ayudó con la batería, pero esta vez en el tipo de batería. Él sugirió que la voz de la batería sonara menos moderna, como con un estilo Glam Rock, y que también tuviese una intención de sonar aunque sea un poco a bandas como Joy Division o The Cure.
            </p>

            <h3>Comentarios</h3>
            <p>
                Esta canción refleja mucho mi postura filosófica de que hay que ser humildes al compartir el conocimiento, una postura muy socrática, a mi parecer. A no dejarse llevar por la idea de que ya sabemos todo o mucho de la realidad, cuando probablemente sepamos menos de lo que podemos llegar a creer. Me refiero a que la ciencia es lo mejor que tenemos para tratar "conocer" la realidad, y es lo mejor que tenemos porque la ciencia no teme, o no debería temer, en contradecir algo que dijo suponía anteriormente. 
            </p>
            <p>                
                Hoy tenemos teorías con un poder explicatívo muy alto, pero es a todo lo que podemos llegar. No por el hecho de tener una teoría que explique muy bien un fenómeno implica que la realidad funcione como la teoría lo dice. A lo que quiero llegar es que no debemos dejar de cuestionar, no debemos dejar que las teorías sean tomadas como verdades absolutas porque eso nos hace arrogantes. Es bastante probable que en el futuro las teorías actuales suenen ridículas comparadas con las futuras. Hay que ser humildes en distinguir lo que conocemos de lo que no conocemos y sólo creemos o suponemos por su alto poder explicativo. Es nuestra condición humana la que no nos permite conocer la realidad en sí misma y mientras ese problema siga vigente, no tenemos por qué vanagloriarnos creyéndonos personas más inteligentes que nuestros antepasados, cuando todo lo que tenmos ahorita, para bien o para mal, es gracias a ellos.
            </p>
            <p>
                &nbsp;&nbsp; Me gusta cómo termina la canción porque es una autoreferencia de que incluso lo que estoy diciendo debe ser examinado.
